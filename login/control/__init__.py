# 如果使用Git
# git 命令
# 1: 在提交代码的时候,需要在当前代码文件夹下面初始化本地仓库 git init
# 2: 将本地代码加入到缓存中 git add .  注意 . 代表将所有文件加入到本地仓库, 如果想加入一个文件,把.改成文件名字就可以
# 3: 提交到本地仓库 git commit -m "提交说明"  如果看到 百分之百说明代码提交成功
# 4: 与远程服务器建立连接 git remote add origin(组织名字) 仓库地址
# 5: 将本地代码提交到远程服务器 git push -u origin(组织名字) 分支名字(默认一般使用主分支,但是主分支一般设置成不让提交代码,是能合并代码,主分支一般是用来打包的)

