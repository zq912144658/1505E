# 注意在开发的时候在向服务器推代码之前,需要先把服务器的代码拉下来
# 使用的命令 git pull 分支名字

# git 分为三个区
# 1: 工作区
# 2: 暂存区也就是缓存区 一般使用命令 git add . 提交代码到里面
# 3: 本地仓库区 一般使用 git commit 来提交代码


# git命令讲解
# 1: 创建仓库的命令 或者初始化仓库的命令 git init
#  如果是使用当前的目录作为仓库就是 git init
#  如果是使用别的目录作为仓库需要 git init 别的目录的直接路径
# 2: 克隆到本地代码的命令 git clone
#  如果想克隆到当前目录 git clone 远程仓库地址
#  如果想克隆到指定的目录里面 git  clone 远程仓库地址 克隆代码的文件夹直接路径
# 3: 如果要查看工作目录里面的内容需要使用 ls命令 展示文件
# 4: 将文件添加到暂存区的命令 git add
#  如果添加所有的文件需要使用到 .
#  如果添加指定的单个文件需要用到 文件名字
